	<p class="small">出典：「内閣府 令和元年版高齢社会白書」（https://www8.cao.go.jp/kourei/whitepaper/w-2019/html/zenbun/s1_2_1.html）</p>
	<p class="u-mt30">また、平成30（2018）年の労働力人口は6,830万人で、そのうち65～69歳の者は450万人、70歳以上の者は425万人であり、労働力人口総数に占める65歳以上の者の割合は12.8％と上昇し続けています。</p>
	<img src="/images/rehire/img02.jpg" width="100%" alt="インストラクターがやさしく指導"/><br>
	<p class="small">出典：「内閣府 令和元年版高齢社会白書」（https://www8.cao.go.jp/kourei/whitepaper/w-2019/html/zenbun/s1_2_1.html）</p>
