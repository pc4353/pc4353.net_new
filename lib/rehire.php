</div>

	
<h2 class="c-title2">パソコンスキルがあれば、定年後も安心</h2>
<div class="rehire-text">
	<h3 class="c-title3">「再雇用」で望まれるスキルとは</h3>
	<p>働きたいという意欲があっても、再雇用の定員には限りがあるもの。同年代で働きたいと考える人も少なくないでしょう。その中で、再雇用の枠に入るためには、これまでの経験や知識・人脈といったもの、それに加えて若者に引けを取らないパソコンスキルを身に付けておけば有利と言えます。</p>
	
	<div class="u-mt30"><h3 class="c-title3">「再就職」でも有利になる</h3></div>
	<div class="u-mb5"><span class="rehire-title-a">■ <span class="rehire-title-b">仕事探し・応募方法はインターネットが主流に</span></span></div>
	<p>最近では、定年後世代向けの求人情報サービスサイトが出てくるなど、インターネットでの求人が増えつつあります。インターネットならば、紙媒体や友人からの紹介等では知りえないような仕事を探し出すことだって可能です。また、企業によっては、履歴書をメールで送付することが求められるケースもありますので、もはやパソコンが使えないと大きな機会損失になってしまうのです。</p>
	
	<div class="u-mt30 u-mb5"><span class="rehire-title-a">■ <span class="rehire-title-b">パソコンスキルがあれば再就職もスムーズ！</span></span></div>
	<p>定年後世代向けの求人数や仕事内容は限られていると言います。仮に興味がある仕事を見つけた場合でも、必ずしも採用に至るとは限りません。そんな時に、パソコンスキルをアピールできれば、採用側もあなたに強い関心を示すこと間違いありません。</p>
	
	<div class="u-mt30 u-mb5"><span class="rehire-title-a">■ <span class="rehire-title-b">今やどの仕事でもパソコンを使うのが当たり前に</span></span></div>
	<p>定年後の働き口として多いのが、事務職やビル・マンションの管理、接客業などです。一見、パソコンとは無縁に思える仕事でも、例えばスタッフ同士の連絡手段はメールが主だったり、売上や在庫、勤怠管理などをパソコンでシステム化しているところも多かったりします。また、簡単なチラシ作成や文書作成が必要になったときにも、パソコン操作は欠かせないものとなります。</p>
</div>



