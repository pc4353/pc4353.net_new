	<p>男女ともに平均寿命が80歳を超え、長寿大国と言われる日本では、<b>「いつまでも働きたい」</b>という考える人が増えています。<br>
	内閣府の発表によると、現在仕事をしている60歳以上の方の約4割が<b>「働けるうちはいつまでも働きたい」</b>と考えていることが見えてきました。70歳くらいまでもしくはそれ以上との回答と合計すれば、約8割が高齢期にも高い就業意欲を持っていることが分かります。</p>
